    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        
        int indice = Modelo.AGENDA.obtenerIndice(jTextFieldId.getText());
        if(indice != -1){
            Persona encontrado = Modelo.AGENDA.buscarPorID(indice);
            jTextFieldNombre.setText(encontrado.getNombre());
            jTextFieldContrase単a.setText(encontrado.getContrase単a());
            jTextFieldCelular.setText(encontrado.getCelular());
            jTextFieldEmail.setText(encontrado.getEmail());
            
        }else{
            JOptionPane.showMessageDialog(this,"El registro con ID N属 " + jTextFieldId.getText() + " no esta resgistrado en el sistema");
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

